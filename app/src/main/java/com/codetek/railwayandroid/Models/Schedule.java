package com.codetek.railwayandroid.Models;

public class Schedule {
    private int id;
    private int location;
    private String slot;
    private String turn;

    public int getId() {
        return id;
    }

    public int getLocation() {
        return location;
    }

    public String getSlot() {
        return slot;
    }

    public String getTurn() {
        return turn;
    }
}
